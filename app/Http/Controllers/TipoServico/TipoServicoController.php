<?php

namespace App\Http\Controllers\TipoServico;

use App\Http\Controllers\Controller;
use App\Http\Service\TipoServico\TipoServicoService;
use Illuminate\Http\Request;

class TipoServicoController extends Controller
{
    protected $tipoServicoService;

    public function __construct(TipoServicoService $tipoServicoService)
    {
        $this->tipoServicoService = $tipoServicoService;
    }

    /**
     * Display a listing of the resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function index()
    {
        return $this->tipoServicoService->list();
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $this->tipoServicoService->register($request);
    }

    /**
     * Display the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function show($id)
    {
        return $this->tipoServicoService->findById($id);
    }

    /**
     * Show the form for editing the specified resource.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        //
    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $this->tipoServicoService->alter($request, $id);
    }

